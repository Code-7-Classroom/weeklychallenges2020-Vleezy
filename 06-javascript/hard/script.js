//variables that will be input into functions
var x = parseInt(prompt('Enter first number here.'))
var y = parseInt(prompt('Enter second number here.'))

//add
function addition (x, y){
    return (x + y);

}
console.log(addition(x, y));

//subtract
function subtraction (x, y){
    return (x - y);

}
console.log(subtraction(x, y));

//multiply
function multiplication (x, y){
    return (x * y);

}
console.log(multiplication(x, y));

//divide
function division (x, y){
    return (x / y);

}
console.log(division(x, y));